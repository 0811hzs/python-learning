#3.2实例属性和类属性的区别
#类属性属于类的，是公共的，大家都能够访问到，实例属性是属于对象的，是私有的
#只能由对象名访问不能由类名访问
#类属性由类名和实例对象名都可以访问，但实例属性只能由实例对象名访问
# print(pe.sex)
#print(person.sex)#会报错
#实例对象只能访问自己定义的属性和公共的属性，别的实例对象添加的属性访问不到
#对于同一个属性，没实例化一次就需要添加一次，效率不高

#构造函数__init__()
#作用:通常用来做属性初始化和赋值操作
#注意：在类实例化对象的时候，会被自动调用
# class Test:
#     def __init__(self):#self--实例对象
#         print("这是__init__()函数")
# te=Test()#运行后会自动调用__init__()函数，输出这是__init__()函数
class Person:
    def __init__(self, name, age,height):
        self.name = name   #这些是实例属性，因为self是实例对象
        self.age = age
        self.height= height
    def play(self):
        print(f"{self.name}在打王者荣耀")
    def introduce(self):
        print(f"我的名字是{self.name}，我的年龄是{self.age}，我的身高是{self.height}cm")
#实例化对象
pe=Person("binghbing",22,190)# 在定义实例化对象的时候会自动调用__init__()函数
pe.play()
pe.introduce()
pe2=Person("李华",20,183 )
pe2.introduce()
pe2.play()
#__init__()该函数是定义实例化对象的时候就自动执行所以需要添加上__init__()函数所需的参数，但如果写的不是__init__()函数，是普通的函数
#的话定义实例化对象的时候不会自动调用函数，需要自己调用，调用的时候再传入函数相关参数
