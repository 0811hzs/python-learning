#面向过程和面向对象的区别
#面向过程相当于手洗衣服：需要实现一个功能的时候，着重的是过程，分析出一个个步骤，并把一个个步骤用一个个函数实现，
# 再依次去调用一个个函数即可（即每一步都需要自己亲历亲为）
#面向对象(类似洗衣机洗衣服）：需要实现一个功能的时候看中的是谁去帮我做这件事情（偷懒，找别人帮我做）
#面向过程是具体顺序，面向对象是具体哪部分负责哪些事情
#2.类和对象
#类就是一系列具有相同属性和行为的事物的统称，不是真实存在的事物
#对象是类的具体实现，是类创建出来的真实存在的事物，面向对象思想的核心
#在开发中先有类再有对象
#2.1类的三要素
#   1，类名
#   2，属性：对象的特征描述，用来说明具体是什么样子的
#   3，方法：对象具有的功能（行为），用来说明能够做什么
#在类中定义的属性是不是描述有什么特征，方法描述有什么功能
#举例:
#类名：人类
#属性：身高 ，年龄，体重
#方法：吃饭，睡觉，玩游戏
#2.2定义类
