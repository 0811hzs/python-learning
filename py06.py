#集合的常见操作
#1.添加元素 add（） ，update()
#add(),添加的是一个整体
# s1={1,2,3,4,5}
# s1.add(6)
# print("现集合",s1)#输出{1, 2, 3, 4, 5, 6}
#由于集合的唯一性，决定如果要添加的元素在集合中已经存在则不进行任何操作
# s1.add(5,6)#会报错，因为一次只能添加一个元素
#若想一次性添加多个只能添加一个元组类型的
