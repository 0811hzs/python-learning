#2.集合具有无序性
# s1={"a","b","c","d","e","f","g","h"}
# print(s1)#每次输出顺序都不同，集合是无序的
# s2={1,2,3,4,5}
# print(s2)#输出{1, 2, 3, 4, 5}，数字运行结果一样

#集合无序的实现方式涉及hash表（了解）
# print(hash("a"))#输出的是字符串a的哈希值
# print(hash("b"))
# print(hash("c"))#python给字符串增加了一层hash随机化保护，输入一个字符串，哈希随机化后
# #每次运行结果都不相同，hash值不同，那么在hash表中的位置也不同，这就实现了集合的无序性
# print(hash(1))
# print(hash(2))
# #在python中int整形的hash值就是其本身，在hash表中位置不会发生改变，所以数顺序也就不会发生改变
# print(hash("1"))
# print(hash("2"))
#用引号括起来的整形变成了字符串类型，所以hash值还是会发生改变


