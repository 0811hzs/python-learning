#删除元素 remove(),pop() ,discard()
#remove():删除集合中的元素，若集合中有就删除，没有就把报错
#s1 = {1, 2, 3, 4, 5}
# s1.remove(1)
# print(s1)#输出{2, 3, 4, 5}
#s1.remove(6)#会报错，集合里面没有6

#pop（）对集合进行无序排列，然后将左边的第一个元素删除
# s1 = {1, 2, 3, 4, 5}
# s1.pop()#因为是数字默认删除第一个元素
# print(s1)#输出{2, 3, 4, 5}
# s2={"a","b","c","d"}
# print(s2)
# s2.pop()#默认删除的是排序后的第一个元素
# print(s2)#


#discard（）：选择要删除的元素，有的话就删除，没有的话不会进行任何操作
# s1 = {1, 2, 3, 4, 5}
# s1.discard(1)
# print(s1)#输出{2, 3, 4, 5}，
# s1.discard(9)
# print(s1)#输出{2, 3, 4, 5}，因为集合中没有9，所以不进行任何操作



